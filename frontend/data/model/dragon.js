export class Dragon {

    constructor(
        id,
        name,
        x,
        y,
        creationDate,
        age,
        color,
        characterId,
        type,
        killerId
    ) {
        this.id = id;
        this.name = name;
        this.x = x;
        this.y = y;
        this.creationDate = creationDate;
        this.age = age;
        this.color = color;
        this.characterId = characterId;
        this.type = type;
        this.killerId = killerId;
    }
}
